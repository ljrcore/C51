void write_english(unsigned char x,unsigned char y, unsigned char en)
{
   unsigned char i=0;	
	werite_x_y(x,y);          //设定起始地址

	//en为索引，每个数字或英文占用16个字节，首地址为(en-1)*32
	for(i=0;i<8;i++) write_data(egk[en*16+i]);	//先写上半个字，共8个字节

	werite_x_y(x+1,y);        //设定起始地址，此次将行地址加1，以便写下行

	for(i=0;i<8;i++) write_data(egk[en*16+8+i]);	//再写下半个字，共8个字节
	
}

