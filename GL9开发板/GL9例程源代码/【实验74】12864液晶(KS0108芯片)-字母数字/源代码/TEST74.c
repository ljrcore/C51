void write_data(unsigned char Rsspdata)
{

	RS=1;				//选择数据寄存器
	RW=0; 				//写选通
	P0=num(Rsspdata);   //将数据写入总线
	_nop_();            //空操作，做短暂的延时
	E=1;            	//E的下降沿，数据写入
	_nop_();            //空操作，做短暂的延时
	E=0;            	//E的下降沿，数据写入
} 

//---------------------------------------
//名称：清屏函数
//适用：给力者GL9单片机开发学习系统
