void init_lcd(void)
{	   
	RST=0;                 //液晶复位
	delay(250);            //适当延时
	RST=1;                 //复位结束
	CS1=1;     				  //左半屏选通
	CS2=1;     				  //右半屏选通	
	delay(250);            //适当延时
	write_com(0x3f);       //显示开
}  
//---------------------------------------
//名称: 主函数
//适用：给力者GL9单片机开发学习系统
//公司：宁波芯动电子有限公司
//网址：www.MovingChip.com
//日期：20120918
//---------------------------------------	
void main()
{  
	unsigned int i,k;
	init_lcd();					  //初始化液晶
  	Clr_Scr();					  //清屏
	while(1)
	{		   	
		if(++i>20000)
    	{
    		i=0;
    		if(++k>50000) k=0;
    		write_english(0,0,k/10000);   				//万位	
			write_english(0,8,(k%10000)/1000);   //千位
			write_english(0,16,(k%1000)/100);   	//百位
			write_english(0,24,(k%100)/10);   		//十位
			write_english(0,32,k%10);   							//个位	
		}
	}
}

