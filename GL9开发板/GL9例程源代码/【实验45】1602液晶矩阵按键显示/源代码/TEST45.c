#include <AT89X52.h>       //调用51单片机的头文件

//---------------------------------------
//4×4矩阵式按键相关I/O设置
sbit HAGN1=P1^0;           //第1行接在P3.0口上
sbit HANG2=P1^1;           //第2行接在P3.1口上
sbit HANB3=P1^2;           //第3行接在P3.2口上
sbit HANG4=P1^3;           //第4行接在P3.3口上
sbit LIE1=P1^4;            //第1列接在P3.4口上
sbit LIE2=P1^5;            //第2列接在P3.5口上
sbit LIE3=P1^6;            //第3列接在P3.6口上
sbit LIE4=P1^7;            //第4列接在P3.7口上
//---------------------------------------

//---------------------------------------
//1602液晶相关I/O设置
sbit E=P2^3;               //1602液晶的E脚接在P2.3口上
sbit RW=P2^4;              //1602液晶的RW脚接在P2.4口上
sbit RS=P2^5;              //1602液晶的RS脚接在P2.5口上
//---------------------------------------

//---------------------------------------
//按键部分用寄存器设置
unsigned char KEYbuf;      //设置8位的寄存器用来暂存按键键值数据
//---------------------------------------

//---------------------------------------
//1602液晶寄存器设置
unsigned char DISbuf;     //设置8位的unsigend char型寄存器用来暂存1602要显示的内容
//---------------------------------------

//---------------------------------------
//名称：1602液晶用延时函数
//适用：给力者GL9单片机开发学习系统
//公司：宁波芯动电子有限公司
