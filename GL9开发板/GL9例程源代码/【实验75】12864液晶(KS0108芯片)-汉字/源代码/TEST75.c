void Clr_Scr(void)
{
	unsigned char j,k;
	CS1=1;     						//左半屏选通
	CS2=1;     						//右半屏选通
	write_com(0x40+0);         //将列地址写入  
		//列地址格式为: 0 1 C5 C4 C3 C2 C1 C0,其中C5-C0=0~0x3f
		//分别代表1~64列
	for(k=0;k<8;k++)
	{
		write_com(0xb8+k);     //将行地址写入	
		//行地址格式为: 1 0 1 1 1 P2 P1 P0,其中P3-C0=0~0x07
		//分别代表1~8行

		for(j=0;j<64;j++) 		//每行的64列都写0，因为选了左右两屏，
		{
			write_data(0x00);  	//64列同时写0
		}
	}
} 
	
//---------------------------------------
//名称：地址写入函数
//适用：给力者GL9单片机开发学习系统
//公司：宁波芯动电子有限公司
//网址：www.MovingChip.com
//日期：20120918
//---------------------------------------
void werite_x_y(unsigned char x,unsigned char y)
{
	y=y&0x7f;     				  //限定范围，列不能超过128
	x=x&0x07;     				  //限定范围，行不能超过8
	if(y<64)                  //如果列小于64
	{
		CS1=1;                 //选通左半屏
		CS2=0;                 //关闭右半屏
		write_com(0x40+y);     //将列地址写入  
		//列地址格式为: 0 1 C5 C4 C3 C2 C1 C0,其中C5-C0=0~0x3f
		//分别代表1~64列
	}
	else
	{
		CS1=0;                 //关闭左半屏
		CS2=1;                 //选通右半屏
		y&=0x3f;
		write_com(0x40+y);     //将列地址写入  
		//列地址格式为: 0 1 C5 C4 C3 C2 C1 C0,其中C5-C0=0~0x3f
		//分别代表1~64列	
	}
	write_com(0xb8+x);     //将行地址写入	
		//行地址格式为: 1 0 1 1 1 P2 P1 P0,其中P3-C0=0~0x07
		//分别代表1~8行
} 

