void write_com(unsigned char cmdcode)
{					   
	RS=0;				//选择命令寄存器
	RW=0; 				//写选通
	P0=num(cmdcode);    //将命令写入总线
	_nop_();            //空操作，做短暂的延时
	E=1;            	//E的下降沿，数据写入
	_nop_();            //空操作，做短暂的延时
	E=0;            	//E的下降沿，数据写入
}

