void write_bmp(void)
{
	unsigned char j=0,i=0;	
	for(j=0;j<8;j++)
	{
		werite_x_y(j,0);
		for(i=0;i<64;i++) write_data(picture[j*128+i]);
		werite_x_y(j,64);
		for(i=64;i<128;i++) write_data(picture[j*128+i]);
	}
}
