void write_chinese(unsigned char x,unsigned char y, unsigned char hz)
{
	unsigned char i=0;	
	werite_x_y(x,y);          //设定起始地址

	//hz为索引，每个汉字占用32个字节，首地址为(hz-1)*32
	for(i=0;i<16;i++) write_data(hzk[hz*32+i]);//先写上半个字，共16个字节
	werite_x_y(x+1,y);        //设定起始地址，此次将行地址加1，以便写下行
	for(i=0;i<16;i++) write_data(hzk[hz*32+16+i]);	//再写下半个字，共16个字节
}
