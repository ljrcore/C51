#include<reg51.h>

//--定义要使用的IO口--//
sbit RELAY = P1^4;  //定义继电器使用的IO口
sbit K1    = P0^0;  //定义按键使用的IO口

/*******************************************************************************
* 函 数 名         : main
* 函数功能		   : 主函数
* 输    入         : 无
* 输    出         : 无
*******************************************************************************/

void main()
{
	while(1)
	{
		if(K1 == 0)	//如果按键按下
		{
			RELAY = 0; //继电器闭合
		}
		else
		{
			RELAY = 1;
		}
	}
}