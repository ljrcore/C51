void write_com(unsigned char cmdcode)
{					   
	RS=0;				//选择命令寄存器
	RW=0; 				//写选通
	P0=num(cmdcode);    //将命令写入总线
	_nop_();            //空操作，做短暂的延时
	E=1;            	//E的下降沿，数据写入
	_nop_();            //空操作，做短暂的延时
	E=0;            	//E的下降沿，数据写入
}
//---------------------------------------
//名称：写数据函数
//适用：给力者GL9单片机开发学习系统
//公司：宁波芯动电子有限公司
//网址：www.MovingChip.com
//日期：20120918
//---------------------------------------
void write_data(unsigned char Rsspdata)
{

	RS=1;				//选择数据寄存器
	RW=0; 				//写选通
	P0=num(Rsspdata);   //将数据写入总线
	_nop_();            //空操作，做短暂的延时
	E=1;            	//E的下降沿，数据写入
	_nop_();            //空操作，做短暂的延时
	E=0;            	//E的下降沿，数据写入
} 
